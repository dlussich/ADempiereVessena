	/**
	 * OpenUp. Issue #829.
	 * Descripcion : Obtiene y retorna lineas de ajuste de inventario.
	 * @return
	 * @throws Exception
	 * @author  Gabriel Vila 
	 * Fecha : 04/08/2011.
	 */
	public MStockAdjustmentLine[] getLines(String trxName) {
		
		String sql = "";
		ResultSet rs = null;
		PreparedStatement pstmt = null;

		List<MStockAdjustmentLine> list = new ArrayList<MStockAdjustmentLine>();
		
		try{
			sql ="SELECT uy_stockadjustmentline_id " + 
 		  	" FROM " + X_UY_StockAdjustmentLine.Table_Name + 
		  	" WHERE uy_stockadjustment_id =?";
			
			pstmt = DB.prepareStatement (sql, trxName);
			pstmt.setInt(1, this.getUY_StockAdjustment_ID());
			
			rs = pstmt.executeQuery ();
		
			while (rs.next()){
				MStockAdjustmentLine value = new MStockAdjustmentLine(Env.getCtx(), rs.getInt(1), trxName);
				list.add(value);
			}
		}
		catch (Exception e)
		{
			log.log(Level.SEVERE, sql, e);
		}
		finally
		{
			DB.close(rs, pstmt);
			rs = null; pstmt = null;
		}
		
		return list.toArray(new MStockAdjustmentLine[list.size()]);		
	}

	@Override
	protected boolean beforeSave(boolean newRecord) {
	
		// Me aseguro que si me cambien el valor del estado de stock en el cabezal, tambien se cambien las lineas
		if (!newRecord){
			MStockAdjustmentLine[] lines = this.getLines(get_TrxName());
			for (int i = 0; i < lines.length; i++){
				lines[i].setUY_StockStatus_ID(this.getUY_StockStatus_ID());
				lines[i].saveEx(get_TrxName());
			}
		}
		
		return true;
	}

